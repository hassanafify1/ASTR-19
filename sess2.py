sum_float = 4.2 + 6.9
print(f"Sum of two floating numbers: {sum_float}")
print(f"Data type of the Result: {type(sum_float)}")

diff_int = 15 - 8
print(f"Difference between two integers: {diff_int}")
print(f"Data type of the Result: {type(diff_int)}")

prod_float = 54.5 * 3
print(f"Product of a floating point number and an integer: {prod_float}")
print(f"Data type of the Result: {type(prod_float)}")